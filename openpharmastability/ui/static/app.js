const form = document.querySelector("#analysis-form");
const fileInput = document.querySelector("#file");
const fileLabel = document.querySelector("#file-label");
const statusTitle = document.querySelector("#status-title");
const statusCopy = document.querySelector("#status-copy");
const statusPill = document.querySelector("#status-pill");
const reportFrame = document.querySelector("#report-frame");
const reportLink = document.querySelector("#report-link");
const pdfBtn = document.querySelector("#pdf-btn");
const emptyPreview = document.querySelector("#empty-preview");
const artifactList = document.querySelector("#artifact-list");
const warningsList = document.querySelector("#warnings-list");
const modeButtons = Array.from(document.querySelectorAll(".segment"));
let attributeMode = "single";

const setStatus = (state, title, copy) => {
  statusPill.className = `status-pill ${state}`;
  statusPill.textContent = state === "ok" ? "Complete" : state[0].toUpperCase() + state.slice(1);
  statusTitle.textContent = title;
  statusCopy.textContent = copy;
};

const text = (selector, value) => {
  document.querySelector(selector).textContent = value ?? "-";
};

fetch("/api/config")
  .then((res) => res.json())
  .then((cfg) => {
    text("#version", `v${cfg.version}`);
    text("#disclaimer-text", cfg.disclaimer);
  })
  .catch(() => {
    text("#disclaimer-text", "Decision-support / educational use only.");
  });

fileInput.addEventListener("change", () => {
  const file = fileInput.files[0];
  fileLabel.textContent = file ? file.name : "Select CSV or XLSX stability data";
});

modeButtons.forEach((button) => {
  button.addEventListener("click", () => {
    attributeMode = button.dataset.mode;
    modeButtons.forEach((b) => b.classList.toggle("active", b === button));
    document.querySelector("#single-attribute-row").hidden = attributeMode !== "single";
    document.querySelector("#attribute-list-row").hidden = attributeMode !== "list";
  });
});

form.addEventListener("submit", async (event) => {
  event.preventDefault();
  const file = fileInput.files[0];
  if (!file) {
    setStatus("error", "Input file required", "Select a CSV or XLSX file before running analysis.");
    return;
  }

  const submit = form.querySelector(".run-button");
  submit.disabled = true;
  setStatus("running", "Running Python analysis", "Generating report artifacts from the authoritative backend.");

  const options = {
    condition: document.querySelector("#condition").value.trim(),
    attribute: document.querySelector("#attribute").value.trim() || "assay",
    attributes: document.querySelector("#attributes").value.trim(),
    all_attributes: attributeMode === "all",
    product_type: document.querySelector("#product-type").value,
    guidance: document.querySelector("#guidance").value,
    bql_policy: document.querySelector("#bql-policy").value,
    assess_transforms: document.querySelector("#assess-transforms").checked,
    run_sensitivity: document.querySelector("#sensitivity").checked,
    run_arrhenius: document.querySelector("#arrhenius").checked,
    run_arrhenius_shelf_life: document.querySelector("#arrhenius-shelf-life").checked,
    run_arrhenius_per_batch: document.querySelector("#arrhenius-per-batch").checked,
    random_effects: document.querySelector("#random-effects").checked,
  };

  if (attributeMode !== "list") {
    options.attributes = "";
  }

  const payload = new FormData();
  payload.append("file", file);
  payload.append("options", JSON.stringify(options));

  try {
    const response = await fetch("/api/analyze", { method: "POST", body: payload });
    const data = await response.json();
    if (!response.ok || data.status !== "ok") {
      throw new Error(data.error || "analysis failed");
    }
    renderResult(data);
  } catch (error) {
    setStatus("error", "Analysis failed", error.message);
  } finally {
    submit.disabled = false;
  }
});

function renderResult(data) {
  const summary = data.summary || {};
  const supported = summary.supported_shelf_life_months == null
    ? "Not limiting"
    : `${summary.supported_shelf_life_months} mo`;

  text("#metric-supported", supported);
  text("#metric-limiting", summary.limiting_attribute || summary.attribute || "-");
  text("#metric-guidance", data.guidance_profile);
  text("#metric-model", formatModel(summary.model || (data.mode === "multi" ? "multi" : "-")));
  text("#audit-status", "Complete");
  text("#audit-mode", data.mode);
  text("#audit-observed", summary.observed_data_months == null ? "-" : `${summary.observed_data_months} mo`);
  text("#audit-crossing", summary.crossing_status || "-");

  setStatus("ok", "Analysis complete", "Report artifacts were generated by the Python backend.");
  renderWarnings(data.warnings || []);
  renderArtifacts(data.artifacts || []);

  const htmlArtifact = (data.artifacts || []).find((item) => item.kind === "html");
  if (htmlArtifact && htmlArtifact.url) {
    reportFrame.src = htmlArtifact.url;
    reportLink.href = htmlArtifact.url;
    reportLink.hidden = false;
    pdfBtn.hidden = false;
    emptyPreview.hidden = true;
  }
}

pdfBtn.addEventListener("click", () => {
  reportFrame.contentWindow.print();
});

function renderWarnings(warnings) {
  warningsList.innerHTML = "";
  if (!warnings.length) {
    const li = document.createElement("li");
    li.textContent = "No analysis warnings reported.";
    warningsList.appendChild(li);
    return;
  }
  warnings.forEach((warning) => {
    const li = document.createElement("li");
    li.textContent = warning;
    warningsList.appendChild(li);
  });
}

function renderArtifacts(artifacts) {
  artifactList.innerHTML = "";
  if (!artifacts.length) {
    const p = document.createElement("p");
    p.className = "muted";
    p.textContent = "No artifacts returned.";
    artifactList.appendChild(p);
    return;
  }
  artifacts.forEach((artifact) => {
    const card = document.createElement("div");
    card.className = "artifact-card";
    const title = document.createElement("strong");
    title.textContent = `${artifact.kind.toUpperCase()} - ${artifact.name}`;
    const meta = document.createElement("span");
    meta.textContent = `${formatBytes(artifact.size_bytes)}${artifact.sha256 ? " - SHA-256 recorded" : ""}`;
    const link = document.createElement("a");
    link.href = artifact.url || "#";
    link.target = "_blank";
    link.rel = "noreferrer";
    link.textContent = "Open";
    const hash = document.createElement("code");
    hash.textContent = artifact.sha256 || "hash unavailable";
    card.append(title, meta, link, hash);
    artifactList.appendChild(card);
  });
}

function formatBytes(value) {
  const bytes = Number(value || 0);
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

function formatModel(value) {
  return String(value || "-")
    .replace("common_slope_batch_intercepts", "common slope + batch intercepts")
    .replace("batch_specific", "batch specific")
    .replace("pooled", "pooled")
    .replaceAll("_", " ");
}
